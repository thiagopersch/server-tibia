local level = 50 -- Limite para liberar uma saga nova

function onSay(cid, words, param, channel)

    if (param == '') then
        doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_BLUE, "Enter the number of the transform.")
        return true
    end

    if not saga[getPlayerVocation(cid)] then
        doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_BLUE,"You can't change transform.")
        return true
    end

    local t = string.explode(param, ",")

    if (t[2]) then
        doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_BLUE, "Could not understand.")
        return true
    end

    if not (tonumber(t[1])) then
        doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_BLUE, "Enter the number of the transform.")
        return true
    end

    if tonumber(t[1]) > #saga[getPlayerVocation(cid)] or tonumber(t[1]) < 1 then
        doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_BLUE, "This transform does not exist.")
        return true
    end

    if getPlayerLevel(cid) >= (tonumber(t[1]) * level) then
        doSetCreatureOutfit(cid, {
            lookType = saga[getPlayerVocation(cid)][tonumber(t[1])]
        }, -1)
        doSendMagicEffect(getThingPos(cid), saga[getPlayerVocation(cid)].effect)
        doPlayerSendTextMessage(cid, 25, "Congratulations!! You chose a new transform!")
        doPlayerSay(cid, "Aaaaahhh!!", TALKTYPE_ORANGE_1)
    else
        doPlayerSendTextMessage(cid, MESSAGE_STATUS_CONSOLE_BLUE, "Sorry, you need level " .. (tonumber(t[1]) * level) .. " to use new transform.")
    end

    return true
end
